_tmplt() {
    local i cur prev opts cmd
    COMPREPLY=()
    cur="${COMP_WORDS[COMP_CWORD]}"
    prev="${COMP_WORDS[COMP_CWORD-1]}"
    cmd=""
    opts=""

    for i in ${COMP_WORDS[@]}
    do
        case "${cmd},${i}" in
            ",$1")
                cmd="tmplt"
                ;;
            tmplt,help)
                cmd="tmplt__help"
                ;;
            tmplt,import)
                cmd="tmplt__import"
                ;;
            tmplt,init)
                cmd="tmplt__init"
                ;;
            tmplt,list)
                cmd="tmplt__list"
                ;;
            tmplt,new)
                cmd="tmplt__new"
                ;;
            tmplt,remove)
                cmd="tmplt__remove"
                ;;
            tmplt,version)
                cmd="tmplt__version"
                ;;
            tmplt__help,help)
                cmd="tmplt__help__help"
                ;;
            tmplt__help,import)
                cmd="tmplt__help__import"
                ;;
            tmplt__help,init)
                cmd="tmplt__help__init"
                ;;
            tmplt__help,list)
                cmd="tmplt__help__list"
                ;;
            tmplt__help,new)
                cmd="tmplt__help__new"
                ;;
            tmplt__help,remove)
                cmd="tmplt__help__remove"
                ;;
            tmplt__help,version)
                cmd="tmplt__help__version"
                ;;
            *)
                ;;
        esac
    done

    case "${cmd}" in
        tmplt)
            opts="-h --help new init import list remove version help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 1 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
